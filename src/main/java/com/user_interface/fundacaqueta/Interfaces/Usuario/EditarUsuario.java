    private boolean esNombreUsuarioValido(){
        boolean esVacio = txtNombreUsuario.getText().equals("");
        return !esVacio;
    }
    
    private boolean esContraseñaValido(){
        boolean esVacio = txtContraseña.getPassword().length <= 0;
        boolean tieneMasDeOchoCaracteres = txtContraseña.getPassword().length > 8;
       
        return !esVacio && tieneMasDeOchoCaracteres;
    }

    private boolean esCorreoValido(){
        boolean esVacio = txtCorreo.getText().equals("");
        boolean contieneArroba = txtCorreo.getText().contains("@");
        boolean contienePunto = txtCorreo.getText().contains(".");
        return !esVacio && contieneArroba && contienePunto;
    }
    
    private boolean esNombresValido(){
        boolean esVacio = txtNombres.getText().equals("");
        return !esVacio;
    }
    
    private boolean esDocumentoValido(){
        boolean esVacio = txtDocumento.getText().equals("");
        return !esVacio;
    }
    
    private void cargarPosiblesErrores(){
        errorChecks.put("El nombre de usuario no puede ser vacio", esNombreUsuarioValido());
        errorChecks.put("La contraseña no puede ser vacia y debe tener mas de 8 caracteres", esContraseñaValido());
        errorChecks.put("El correo no puede ser vacio y debe ser un correo valido \"ejemplo@ejemplo.dom\"", esCorreoValido());
        errorChecks.put("Los nombres del usuario no pueden ser vacíos", esNombresValido());
        errorChecks.put("El documento del usuario no puede ser vacío y debe ser un número entero, no incluya puntos", esDocumentoValido());
    }
    
    private boolean lanzarErrores(){
        for(String error : errorChecks.keySet()){
            if(!errorChecks.get(error)){
                JOptionPane.showMessageDialog(this,
                        error,
                        "Error", JOptionPane.ERROR_MESSAGE);
                return true;
            }
        }
        return false;
    }
    
    private String obtenerContraseñaEnString(){
        String contraseña = "";
            for(int i = 0; i < txtContraseña.getPassword().length; i++){
                contraseña += txtContraseña.getPassword()[i];
            }
        return contraseña;
    }
