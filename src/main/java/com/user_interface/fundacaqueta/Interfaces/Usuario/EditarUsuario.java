    private void bttnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttnConfirmarActionPerformed
        // TODO add your handling code here:
        cargarPosiblesErrores();
        if(!lanzarErrores()){
            String contraseña = obtenerContraseñaEnString();
            if(tipoUsuario == Usuario.ADMINISTRADOR)
                admin.editarUsuarioAdministrador(
                        txtNombreUsuario.getText(),
                        contraseña,
                        txtNombres.getText(),
                        txtCorreo.getText(), 
                        Integer.parseInt(txtDocumento.getText())
                );
            else
                admin.editarUsuarioAuxiliar(
                        txtNombreUsuario.getText(),
                        contraseña,
                        txtNombres.getText(),
                        txtCorreo.getText(),
                        Integer.parseInt(txtDocumento.getText())
                );
            
            JOptionPane.showMessageDialog(this,
                   "Usuario editado con éxito",
                   "Usuario editado",
                   JOptionPane.INFORMATION_MESSAGE
            );
            
            this.dispose();
        }
    }//GEN-LAST:event_bttnConfirmarActionPerformed

